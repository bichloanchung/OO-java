package com.classes;

public class Calculator {
	public int addition(int firstNumber, int secondNumber) {
		return firstNumber + secondNumber;
	}
	public int substraction(int firstNumber, int secondNumber) {
		return firstNumber - secondNumber;
	}
	public double addition(double firstNumber, double secondNumber) {
		return firstNumber + secondNumber;
	}
	public double substraction(double firstNumber, double secondNumber) {
		return firstNumber - secondNumber;
	}
	public float addition(float firstNumber, float secondNumber) {
		return firstNumber + secondNumber;
	}
	public float substraction(float firstNumber, float secondNumber) {
		return firstNumber - secondNumber;
	}

}
